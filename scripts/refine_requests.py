"""
관리자용 요청 정제 도구 + 자동 페르소나 프롬프트 생성기 (refine_requests.py)

사용 방법:
    python scripts/refine_requests.py

기능:
  1) data/student/user_requests.json 에서 학생 요청을 불러옵니다.
  2) 각 요청을 터미널에 출력하고 반영 여부를 관리자가 직접 결정합니다.
  3) 승인된 요청들을 집계해 "입시 시스템 수석 아키텍트" 페르소나 지시문을
     최상단에 삽입한 claude_patch_prompt.md 를 생성합니다.
  4) 생성된 프롬프트는 Claude Code 에 그대로 붙여넣으면
     추가 확인 없이 단일 패스로 즉시 실행됩니다.
"""

import json
import sys
import re
import textwrap
from datetime import datetime
from pathlib import Path

# ─────────────────────────────────────────────────────────────
# 경로 상수
# ─────────────────────────────────────────────────────────────

_루트          = Path(__file__).parent.parent
_요청_파일      = _루트 / "data" / "student" / "user_requests.json"
_출력_파일      = _루트 / "data" / "student" / "claude_patch_prompt.md"
_대학_목록_파일 = _루트 / "data" / "university_list.txt"
_파싱_JSON     = _루트 / "data" / "student" / "parsed_admission_guide.json"
_스크립트_디렉터리 = _루트 / "scripts"

# ─────────────────────────────────────────────────────────────
# 터미널 색상 (ANSI)
# ─────────────────────────────────────────────────────────────

class 색:
    굵게   = "\033[1m"
    파랑   = "\033[94m"
    초록   = "\033[92m"
    노랑   = "\033[93m"
    빨강   = "\033[91m"
    회색   = "\033[90m"
    청록   = "\033[96m"
    보라   = "\033[95m"
    초기화 = "\033[0m"
    구분선 = "\033[36m"


def 출력(메시지: str, 색상: str = "") -> None:
    print(f"{색상}{메시지}{색.초기화}" if 색상 else 메시지)


def 구분선_출력(문자: str = "━", 길이: int = 62) -> None:
    출력(문자 * 길이, 색.구분선)


# ─────────────────────────────────────────────────────────────
# 요청 파일 로더
# ─────────────────────────────────────────────────────────────

def 요청_로드() -> list[dict]:
    """data/student/user_requests.json 에서 요청 목록을 반환합니다."""
    if not _요청_파일.exists():
        출력(f"[오류] 요청 파일이 없습니다: {_요청_파일}", 색.빨강)
        출력("  → 텔레그램 봇을 통해 학생이 요청을 보내면 파일이 자동 생성됩니다.", 색.회색)
        return []
    try:
        with open(_요청_파일, encoding="utf-8") as f:
            data = json.load(f)
        요청들 = data.get("requests", [])
        출력(f"[로드] 총 {len(요청들)}개 요청을 불러왔습니다.", 색.초록)
        return 요청들
    except json.JSONDecodeError as e:
        출력(f"[오류] JSON 파싱 실패: {e}", 색.빨강)
        return []


# ─────────────────────────────────────────────────────────────
# 요청 화면 출력
# ─────────────────────────────────────────────────────────────

def 요청_출력(요청: dict, 번호: int, 전체: int) -> None:
    """요청 내용을 보기 좋게 터미널에 출력합니다."""
    구분선_출력()
    출력(f"  요청 {번호}/{전체}", 색.굵게)
    구분선_출력("─")
    출력(f"  요청 ID    : {요청.get('request_id', '?')}", 색.파랑)
    출력(f"  접수 시각  : {요청.get('timestamp', '?')[:16].replace('T', ' ')}", 색.파랑)
    출력(f"  학생 이름  : {요청.get('full_name', '?')}", 색.파랑)
    출력(f"  chat_id    : {요청.get('chat_id', '?')}", 색.회색)
    출력(f"  요청 유형  : {요청.get('유형', '기타')}", 색.노랑)
    출력(f"  처리 상태  : {요청.get('처리_상태', '?')}", 색.회색)
    구분선_출력("─")
    출력("  [요청 내용]", 색.굵게)
    내용 = 요청.get("내용", "(내용 없음)")
    for 줄 in (내용[i:i+70] for i in range(0, len(내용), 70)):
        출력(f"    {줄}")
    구분선_출력("─")


# ─────────────────────────────────────────────────────────────
# 요청 메타데이터 분석 헬퍼
# ─────────────────────────────────────────────────────────────

_대학_패턴 = re.compile(
    r"(서울대|연세대|고려대|서강대|성균관대|한양대|중앙대|경희대"
    r"|이화여대|이화여자대학교|서울시립대|숭실대|광운대|한국외대"
    r"|외국어대|국민대|동국대|건국대|홍익대|숙명여대|세종대|단국대"
    r"|인하대|부산대|경북대|아주대|충남대|전남대|성신여대|덕성여대"
    r"|가천대|인천대|포항공대|포항공과대|DGIST|GIST|UNIST"
    r"|[가-힣]{2,6}(?:대학교|대학|여대))"
)

_카테고리_규칙: list[tuple[str, list[str]]] = [
    ("연도별_데이터_입력", ["학년", "학년도", "연도", "년도"]),
    ("정시_분석_추가",    ["정시", "n수", "재수", "반수", "수능"]),
    ("정책_분석_기능",    ["정책", "교육정책", "대입정책", "교육부"]),
    ("대학_데이터_추가",  ["대학교", "대학", "캠퍼스", "입학처"]),
    ("기능_개선",         ["기능", "추가", "개선", "업데이트", "버그"]),
]


def 대학명_추출(텍스트: str) -> list[str]:
    """요청 텍스트에서 언급된 대학명을 추출합니다 (중복 제거)."""
    return list(dict.fromkeys(_대학_패턴.findall(텍스트)))


def 기능_분류(요청: dict) -> str:
    """요청 내용을 기반으로 기능 카테고리를 반환합니다."""
    내용 = (요청.get("내용") or "").lower()
    for 카테고리, 키워드들 in _카테고리_규칙:
        if any(k in 내용 for k in 키워드들):
            return 카테고리
    return "기타_요청"


def 카테고리_우선순위(카테고리: str) -> int:
    """태스크 실행 우선순위를 반환합니다 (낮을수록 먼저)."""
    순서 = {
        "대학_데이터_추가":  1,
        "연도별_데이터_입력": 2,
        "정시_분석_추가":    3,
        "정책_분석_기능":    4,
        "기능_개선":         5,
        "기타_요청":         6,
    }
    return 순서.get(카테고리, 99)


# ─────────────────────────────────────────────────────────────
# 시스템 현황 조회
# ─────────────────────────────────────────────────────────────

def 시스템_현황_조회() -> dict:
    """현재 파싱 완료 대학·수집 대상·PDF 파일 수를 반환합니다."""
    현황: dict = {
        "파싱_완료": [],
        "파싱_실패": [],
        "수집_대상": [],
        "총_PDF_수": 0,
        "스크립트_목록": [],
    }

    # parsed_admission_guide.json
    if _파싱_JSON.exists():
        try:
            with open(_파싱_JSON, encoding="utf-8") as f:
                d = json.load(f)
            for item in d.get("대학_목록", []):
                name = item.get("대학명", "").strip()
                if item.get("파싱_실패"):
                    현황["파싱_실패"].append(name)
                else:
                    현황["파싱_완료"].append(name)
        except Exception:
            pass

    # university_list.txt
    if _대학_목록_파일.exists():
        lines = _대학_목록_파일.read_text(encoding="utf-8").splitlines()
        현황["수집_대상"] = [l.strip() for l in lines if l.strip() and not l.startswith("#")]

    # raw_pdf/
    raw_dir = _루트 / "data" / "raw_pdf"
    if raw_dir.exists():
        현황["총_PDF_수"] = len(list(raw_dir.glob("*.pdf")))

    # scripts/ 파일 목록
    if _스크립트_디렉터리.exists():
        현황["스크립트_목록"] = [f.name for f in _스크립트_디렉터리.glob("*.py")]

    return 현황


